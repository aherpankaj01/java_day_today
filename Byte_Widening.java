class  Byte_Widening
{
	public static void main(String[] args) 
	{
		double b1= 100;  //100.0
		System.out.println(b1);
		
		float b2= 100;  //100.0
		System.out.println(b2);
		
		long b3= 100;  //100
		System.out.println(b3);
		
		int b4= 100;  //100
		System.out.println(b4);
		
		char b5= 100;  //d
		System.out.println(b5);
		
		short b6= 100;  //100
		System.out.println(b6);
		
		
	}
}